void flags_to_string(char* buf, uint8_t flag_mask) {
    if (!flag_mask) {
        strcpy(buf, "<none>");
        return;
    }
    int pos = 0;
    buf[pos++] = '#';
    if (flag_mask & FLAG_SF) buf[pos++] = 'n';
    if (flag_mask & FLAG_ZF) buf[pos++] = 'z';
    if (flag_mask & FLAG_CF) buf[pos++] = 'c';
    if (flag_mask & FLAG_OF) buf[pos++] = 'v';
    if (flag_mask & (FLAG_PF_LO | FLAG_PF_HI)) buf[pos++] = 'p';
    if (flag_mask & FLAG_AF) buf[pos++] = 'a';
    if (flag_mask & 0x01) buf[pos++] = 'd';
    buf[pos] = '\0';
}

